def multiples_of_3_and_5(n):
   threes = [ num for num in range(n) if num%3==0 ]
   fives = [ num for num in range(n) if num%5==0 and num%3 != 0]
   return sum(threes) + sum(fives)
   #must REMOVE DUPLICATES 


